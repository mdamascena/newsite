import { toast } from 'react-toastify';

export const toastWarningColored = (message, options = {}) => {
    toast.warn(message, {
        position: "top-right",
        autoClose: 5000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        theme: "colored",
        ...options,
    });
};

